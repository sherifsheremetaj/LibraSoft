    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
          DefaultTableModel model=(DefaultTableModel) Tb.getModel();
        if(!name.getText().trim().equals("")&&!edition.getText().trim().equals("")&&!id.getText().trim().equals("")){
            model.addRow(new Object[]{id.getText(),name.getText(),edition.getText(),year.getText(),issue.getText()});

            try{



                Class.forName("com.mysql.jdbc.Driver");
                Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/lib","root","");
                PreparedStatement ps=conn.prepareStatement("INSERT INTO issue (ID,Name,Edition,Year,Issues)values(?,?,?,?,?)");

                ps.setString(1,id.getText());
                ps.setString(2,name.getText());
                  ps.setString(3,edition.getText());
             
                ps.setString(4,year.getText());
               
                ps.setString(5,issue.getText());
                
                int i=ps.executeUpdate();
    
                if(i>0){
                    JOptionPane.showMessageDialog(null,"Successfully saved  ");
                    id.setText("");
                    name.setText("");
                    edition.setText("  /10");
         
                    year.setText("");
                    issue.setText("");
                 
                    ps.close();
                }else{
                    JOptionPane.showMessageDialog(null," Not saved ");

                }
            }
            catch (Exception e){
                e.printStackTrace();
            }
        }
        else {
            JOptionPane.showMessageDialog(null,"Please fill ID,Name and Edition first ");
        }

    fetch();

    }                                   
