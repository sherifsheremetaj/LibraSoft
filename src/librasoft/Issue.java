    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(name.getText().trim().equals("")&&edition.getText().trim().equals("")&&id.getText().trim().equals("")){
         JOptionPane.showMessageDialog(null,"Please fill Name,Surname & ID first ");
    }else{
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/lib","root","");
            String a="update issue set ID=?,Name=?,Edition=?,Year=?,Issues=? ";
            PreparedStatement ps=conn.prepareStatement(a);
            ps.setString(1,id.getText());
              ps.setString(2,name.getText());
                ps.setString(3,edition.getText());
          
            ps.setString(4,year.getText());
    
            ps.setString(5,issue.getText());
            

            int rw=ps.executeUpdate();
            if(rw>0){
                JOptionPane.showMessageDialog(null,"Changes are saved");

            }
            else{
                JOptionPane.showMessageDialog(null,"Changes are not saved");

            }
        }
        catch(Exception e){
            Logger.getLogger(LibraSoft.class.getName()).log(Level.SEVERE, null, e);
        }
        DefaultTableModel model=(DefaultTableModel) Tb.getModel();
        model.setValueAt(id.getText(),Tb.getSelectedRow(),0);
         model.setValueAt(name.getText(),Tb.getSelectedRow(),1);
          model.setValueAt(edition.getText(),Tb.getSelectedRow(),2);
       
        model.setValueAt(year.getText(),Tb.getSelectedRow(),3);
     
        model.setValueAt(issue.getText(),Tb.getSelectedRow(),4);
     

        Tb.setDefaultEditor(Object.class, null);}
    }//GEN-LAST:event_jButton3ActionPerformed

